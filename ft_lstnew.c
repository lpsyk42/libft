#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*ptr;

	ptr = (t_list *)malloc(sizeof(t_list));
	if (!ptr)
		return (ptr = NULL);
	ptr->next = NULL;
	ptr->content = content;
	return (ptr);
}
